create table if not exists public.cost_versions (
  id bigint generated by default as identity primary key,
  cost_id uuid not null,
  project_id bigint not null references public.projects(id) on delete cascade,
  owner_id bigint references public.owners(id) on delete set null,
  version integer not null check (version > 0),
  name text not null,
  amount numeric(12,2) not null check (amount > 0),
  phase text not null check (phase in ('development', 'construction')),
  cost_date date not null,
  attachments jsonb not null default '[]'::jsonb check (jsonb_typeof(attachments) = 'array'),
  deleted_at timestamptz,
  created_by uuid references auth.users(id) on delete set null default auth.uid(),
  created_at timestamptz not null default now(),
  unique (cost_id, version)
);

create table if not exists public.incomes (
  id bigint generated by default as identity primary key,
  project_id bigint not null references public.projects(id) on delete cascade,
  description text not null,
  source text not null,
  amount numeric(12,2) not null check (amount > 0),
  income_date date not null,
  income_type text not null default 'project_income'
    check (income_type in ('project_income', 'refund', 'reimbursement', 'other')),
  deleted_at timestamptz,
  created_by uuid references auth.users(id) on delete set null default auth.uid(),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.review_items (
  id bigint generated by default as identity primary key,
  project_id bigint not null references public.projects(id) on delete cascade,
  owner_id bigint references public.owners(id) on delete set null,
  category_id bigint references public.cost_categories(id) on delete set null,
  source_name text,
  vendor text,
  amount numeric(12,2),
  transaction_date date,
  description text,
  entry_type text not null default 'unknown' check (entry_type in ('deposit', 'debit', 'unknown')),
  notes text,
  status text not null default 'pending' check (status in ('pending', 'approved', 'removed')),
  raw_data jsonb not null default '{}'::jsonb,
  created_by uuid references auth.users(id) on delete set null default auth.uid(),
  created_at timestamptz not null default now(),
  reviewed_at timestamptz,
  reviewed_by uuid references auth.users(id) on delete set null
);

create table if not exists public.documents (
