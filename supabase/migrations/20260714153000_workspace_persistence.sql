create or replace view public.active_costs
with (security_invoker = true)
as
select latest.*
from (
  select distinct on (cost_id) *
  from public.cost_versions
  order by cost_id, version desc
) latest
where latest.deleted_at is null;

create or replace function public.create_cost_version(
  p_project_id bigint,
  p_cost_id uuid,
  p_owner_id bigint,
  p_name text,
  p_amount numeric,
  p_phase text,
  p_cost_date date,
  p_attachments jsonb default '[]'::jsonb,
  p_deleted boolean default false
)
returns public.cost_versions
language plpgsql
security invoker
set search_path = ''
as $$
declare
  next_version integer;
  result public.cost_versions;
begin
  if not private.can_manage_project(p_project_id) then
    raise exception 'You do not have permission to manage this project';
  end if;

  perform pg_advisory_xact_lock(hashtextextended(p_cost_id::text, 0));
  select coalesce(max(version), 0) + 1 into next_version
  from public.cost_versions
  where cost_id = p_cost_id and project_id = p_project_id;

  insert into public.cost_versions (
    cost_id, project_id, owner_id, version, name, amount, phase,
    cost_date, attachments, deleted_at
  ) values (
    p_cost_id, p_project_id, p_owner_id, next_version, trim(p_name), p_amount,
    p_phase, p_cost_date, coalesce(p_attachments, '[]'::jsonb),
    case when p_deleted then now() else null end
  ) returning * into result;

  return result;
end;
$$;

create or replace function public.approve_review_item(p_item_id bigint, p_category_id bigint)
returns public.transactions
language plpgsql
security invoker
set search_path = ''
as $$
declare
  item public.review_items;
  project_company_id uuid;
  result public.transactions;
begin
  select * into item from public.review_items where id = p_item_id for update;
  if item.id is null or item.status <> 'pending' then
    raise exception 'This review item is no longer pending';
  end if;
  if not private.can_manage_project(item.project_id) then
    raise exception 'You do not have permission to manage this project';
  end if;
  if not exists (
    select 1 from public.cost_categories c
    where c.id = p_category_id and c.project_id = item.project_id
  ) then
    raise exception 'Select a category from this project';
  end if;

  select company_id into project_company_id from public.projects where id = item.project_id;
  insert into public.transactions (
    company_id, project_id, category_id, date, description, amount,
    source, matched_invoice_id, raw_import_row
  ) values (
    project_company_id, item.project_id, p_category_id,
    coalesce(item.transaction_date, current_date),
    coalesce(nullif(item.description, ''), nullif(item.vendor, ''), nullif(item.source_name, ''), 'Reviewed document'),
    coalesce(item.amount, 0), 'manual', null,
    jsonb_build_object('reviewItemId', item.id, 'sourceName', item.source_name, 'vendor', item.vendor)
  ) returning * into result;

  update public.review_items
  set status = 'approved', category_id = p_category_id, reviewed_at = now(), reviewed_by = auth.uid()
  where id = item.id;

  return result;
end;
$$;

create or replace function private.seed_project_categories()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
begin
  insert into public.cost_categories (project_id, phase, name, budgeted_amount)
  values
    (new.id, 'development', 'Development', 0),
    (new.id, 'construction', 'Construction', 0);
  return new;
end;
$$;

drop trigger if exists on_project_created_seed_categories on public.projects;
create trigger on_project_created_seed_categories
  after insert on public.projects
  for each row execute function private.seed_project_categories();

insert into public.cost_categories (project_id, phase, name, budgeted_amount)
select p.id, defaults.phase, defaults.name, 0
from public.projects p
cross join (values ('development', 'Development'), ('construction', 'Construction')) defaults(phase, name)
where not exists (
  select 1 from public.cost_categories c
  where c.project_id = p.id and c.phase = defaults.phase and lower(c.name) = lower(defaults.name)
);

