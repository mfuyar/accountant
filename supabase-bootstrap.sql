drop policy if exists transactions_company_isolation on transactions;
create policy transactions_company_isolation on transactions
  for all
  using (
    company_id = public.current_company_id()
    and exists (
      select 1 from projects p
      where p.id = transactions.project_id and p.company_id = public.current_company_id()
    )
  )
  with check (
    company_id = public.current_company_id()
    and exists (
      select 1 from projects p
      where p.id = transactions.project_id and p.company_id = public.current_company_id()
    )
  );

drop policy if exists payments_company_isolation on payments;
create policy payments_company_isolation on payments
  for all
  using (exists (
    select 1
    from invoices i
    join projects p on p.id = i.project_id
    where i.id = payments.invoice_id and p.company_id = public.current_company_id()
  ))
  with check (exists (
    select 1
    from invoices i
    join projects p on p.id = i.project_id
    where i.id = payments.invoice_id and p.company_id = public.current_company_id()
  ));

