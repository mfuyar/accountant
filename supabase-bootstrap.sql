drop policy if exists companies_company_isolation on companies;
create policy companies_company_isolation on companies
  for select using (id = public.current_company_id());

drop policy if exists profiles_self_access on profiles;
create policy profiles_self_access on profiles
  for select using (id = auth.uid());

drop policy if exists projects_company_isolation on projects;
create policy projects_company_isolation on projects
  for all
  using (company_id = public.current_company_id())
  with check (company_id = public.current_company_id());

drop policy if exists owners_company_isolation on owners;
create policy owners_company_isolation on owners
  for all
  using (exists (
    select 1 from projects p
    where p.id = owners.project_id and p.company_id = public.current_company_id()
  ))
  with check (exists (
    select 1 from projects p
    where p.id = owners.project_id and p.company_id = public.current_company_id()
  ));

